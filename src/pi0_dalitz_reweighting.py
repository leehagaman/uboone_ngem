"""Compute per-event pi0 Dalitz-decay reweighting weights (Geant4 -> EvtGen).

This module replicates the weight-producing logic of
``ipynb_notebooks/pi0_dalitz_reweighting.ipynb`` so that
``pi0_dalitz_reweighting.parquet`` can be regenerated as part of the main
dataframe-creation pipeline instead of by hand.

Physics.  A pi0 -> e+ e- gamma Dalitz decay at rest has only two dynamical
degrees of freedom: the dilepton invariant mass ``m_ee`` and the lepton helicity
angle ``cos(theta*)``, both invariant under the pi0 lab boost.  Geant4's
``G4DalitzDecayChannel`` samples the lepton angle isotropically (no 1+cos^2 term),
whereas EvtGen's ``PI0_DALITZ`` (Kroll-Wada QED) peaks at the poles.  The entire
Geant4-vs-EvtGen difference is therefore captured by a single 2D weight
``w(m_ee, cos theta*) = rho_EvtGen / rho_Geant4`` built from high-statistics
pure-model samples:
  source = standalone Geant4 (1M, geant4_pi0_dalitz.csv),
  target = EvtGen          (1M, evtgen_pi0_dalitz.csv).
See https://doi.org/10.1103/PhysRevC.95.025202 for the approximate cos(theta*)
analytic prediction.

Two halves:
- ``compute_pi0_dalitz_reweighting`` uses *only* the two pure-model CSV samples to
  build the bin-by-bin weight grid w(m_ee, cos theta*) = EvtGen / Geant4 (a density
  ratio, so <w> = 1 over the Geant4 model by construction), and saves it to
  ``pi0_dalitz_reweighting.parquet`` (one row per bin: mee_bin, cos_bin, mee_lo,
  mee_hi, cos_lo, cos_hi, weight).  It also makes the weight-map plot.
- ``apply_pi0_dalitz_reweighting`` reads that grid and, for every truth pi0 Dalitz
  decay in the df, finds the (m_ee, cos theta*) bin the event falls in and applies
  its weight -- adding a standalone ``pi0_dalitz_reweight_weight`` column (1.0 for
  non-Dalitz events) and multiplying it into ``wc_net_weight``.  It also rebuilds the
  full closure / lab-frame monitoring plots (overlaying the real BNB Dalitz events on
  the Geant4/EvtGen reference curves).

The per-event ``(m_ee, cos theta*)`` are computed once, for every truth pi0 Dalitz
decay (including the partially-saved ones, with missing-lepton recovery), in
``do_wc_postprocessing`` (postprocessing.py), and stored as the scalar df columns
``wc_true_pi0_dalitz_m_ee`` / ``wc_true_pi0_dalitz_cos_theta_star`` (plus the
gamma/e+/e- lab four-vectors, used only to rebuild the monitoring plots and then
dropped by apply).  apply does a straight per-row bin lookup on those columns, so
there is no join (and hence no issue with run/subrun/event colliding across the
derived overlay copies): each row is weighted from its own truth invariants.
"""

